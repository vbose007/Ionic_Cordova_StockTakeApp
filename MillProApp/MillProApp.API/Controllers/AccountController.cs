        //// POST: api/Account/RequestReset
        [AllowAnonymous]
        [Route("RequestReset")]
        [HttpPost]
        public IHttpActionResult  RequestReset([FromBody] ForgotPasswordViewModel model)
        {
            if (ModelState.IsValid)
            {
                var user = UserManager.FindByName(model.Email);
                if (user == null)
                {
                    return
                        ResponseMessage(Request.CreateErrorResponse(HttpStatusCode.NotFound,
                            $"No user with email {model.Email} was not found on our system. Please enter a valid user email."));
                }

                // For more information on how to enable account confirmation and password reset please visit http://go.microsoft.com/fwlink/?LinkID=320771
                // Send an email with this link

                string code = UserManager.GeneratePasswordResetToken(user.Id);

                var uri = new UriBuilder();

                uri.Host = Request.Headers.Host;

                uri.Scheme = Request.RequestUri.Scheme;

                uri.Path = "UserAccount/ResetPassword";

                uri.Query = $"userId={user.Id}&&code={HttpUtility.UrlEncode(code)}";

                var callbackUrl = uri.Uri.ToString();

                Task.WaitAll(UserManager.SendEmailAsync(user.Id, "Reset Password", "Please reset your password by clicking <a href=\"" + callbackUrl + "\">here</a>"));

                return Ok("Instructions to reset your password have been sent to your email address.");
            }

            // If we got this far, something failed, return error
            return BadRequest("The email address provided was either not found on our system or is invalid.");
                
        }

        //Post api/Account/Login
        [Route("Login")]
        [HttpPost]
        public IHttpActionResult Login([FromBody] LoginRequestModel loginModel)
        {
            if (!ModelState.IsValid)
            {
                return BadRequest(ModelState);
            }


            var userManager = HttpContext.Current.GetOwinContext().Get<ApplicationUserManager>();

            var user = userManager.FindByName(loginModel.UserName);

            if (user == null)
            {
                return NotFound();
            }

            bool isValiLogin = userManager.CheckPassword(user, loginModel.Password);

            if (isValiLogin)
            {
                Authentication.SignIn();

                return Ok();
            }

            return ResponseMessage(Request.CreateErrorResponse(HttpStatusCode.Unauthorized,
                "Login failed. Invalid username or password."));

        }

        [AllowAnonymous]
        [HttpGet]
        [Route("checkUserNameTaken")]
        public JsonResult<bool> GetIsUserNameTaken([FromUri]string username)
        {
            if (string.IsNullOrWhiteSpace(username)) return Json(false);
            

            var user = UserManager.FindByName(username);

            return Json(user != null);
        }



        // GET api/Account/ManageInfo?returnUrl=%2F&generateState=true
        [Route("ManageInfo")]
        public async Task<ManageInfoViewModel> GetManageInfo(string returnUrl, bool generateState = false)
        {
            IdentityUser user = await UserManager.FindByIdAsync(User.Identity.GetUserId());

            if (user == null)
            {
                return null;
            }

            List<UserLoginInfoViewModel> logins = new List<UserLoginInfoViewModel>();

            foreach (IdentityUserLogin linkedAccount in user.Logins)
            {
                logins.Add(new UserLoginInfoViewModel
                {
                    LoginProvider = linkedAccount.LoginProvider,
                    ProviderKey = linkedAccount.ProviderKey
                });
            }

            if (user.PasswordHash != null)
            {
                logins.Add(new UserLoginInfoViewModel
                {
                    LoginProvider = LocalLoginProvider,
                    ProviderKey = user.UserName,
                });
            }

            return new ManageInfoViewModel
            {
                LocalLoginProvider = LocalLoginProvider,
                Email = user.UserName,
                Logins = logins,
                ExternalLoginProviders = GetExternalLogins(returnUrl, generateState)
            };
        }

